    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed

        ArrayList<OrderList> listOfOrders = business.getMasterOrderCatalog().getOrderList();
        Map<String, ArrayList<Double>> map = new HashMap();
        double checkedPrice;
        double targetPrice;
        double actualPrice;
        ArrayList<Double> allPrices;
        for (String p : salesPerson) {
            checkedPrice = 0;
            targetPrice = 0;
            actualPrice = 0; 
            allPrices = new ArrayList();
            for (OrderList ol : listOfOrders) {
                for (Order o : ol.getOrderList()) {
                    if (o.getSalesPerson().getSalesPersonName().equals(p)) {
                        checkedPrice = checkedPrice + (o.getQuantity() * o.getSalesPrice());
                        targetPrice = targetPrice + (o.getQuantity() * (o.getProduct().getPrice() * 1.2));
                        actualPrice = actualPrice +  (o.getQuantity() * (o.getProduct().getPrice()));
                    }
                }
            }
            allPrices.add(checkedPrice);
            allPrices.add(targetPrice);
            allPrices.add(actualPrice);
            map.put(p, allPrices);
        }
        Map<String, ArrayList<Double>> aboveTargetSales = new HashMap();
        Map<String, ArrayList<Double>> belowTargetSales = new HashMap();
        ArrayList<Double> abovePrices;
        ArrayList<Double> belowPrices;
        for (Map.Entry<String, ArrayList<Double>> entry : map.entrySet()) {
            abovePrices = new ArrayList();
            belowPrices = new ArrayList();        
            if (entry.getValue().get(0) > entry.getValue().get(1)) {
                abovePrices.add(entry.getValue().get(0));
                abovePrices.add(entry.getValue().get(1));
                abovePrices.add(entry.getValue().get(2));
                aboveTargetSales.put(entry.getKey(),abovePrices);
            } else if (entry.getValue().get(0) < entry.getValue().get(1)) {
                belowPrices.add(entry.getValue().get(0));
                belowPrices.add(entry.getValue().get(1));
                belowPrices.add(entry.getValue().get(2));
                belowTargetSales.put(entry.getKey(),belowPrices);
            }
        }

        if (marketNameBox.getSelectedItem() == "Above Target Sale") {
            populateTable(aboveTargetSales);
        } else if (marketNameBox.getSelectedItem() == "Below Target Sale") {
            populateTable(belowTargetSales);
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    public void populateTable(Map<String, ArrayList<Double>>  list) {
        DefaultTableModel model = (DefaultTableModel) prodJTable.getModel();
        model.setRowCount(0);
        for (Map.Entry<String, ArrayList<Double>> entry : list.entrySet()) {
            Object row[] = new Object[6];
            row[0] = entry.getKey();
            row[1] = entry.getValue().get(0);
            row[2] = entry.getValue().get(1);
            row[3] = entry.getValue().get(2);
            row[4] = entry.getValue().get(1)-entry.getValue().get(2);
            row[5] = entry.getValue().get(0)-entry.getValue().get(1);
            model.addRow(row);
        }
    }

