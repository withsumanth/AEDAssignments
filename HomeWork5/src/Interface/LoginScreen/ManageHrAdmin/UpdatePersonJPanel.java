    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String fName = fNameTxt.getText();
        String lName = lNameTxt.getText();
        if(fName.trim().length()==0 || lName.trim().length()==0){
            JOptionPane.showMessageDialog(null, "Please enter all the fields");
            return;
        }
        if (!(Pattern.matches("^[a-zA-Z]+$", fNameTxt.getText())) || !(Pattern.matches("^[a-zA-Z]+$", lNameTxt.getText()))) {
    JOptionPane.showMessageDialog(null, "Please enter a valid characterfor first name and last name");
    return;
}
        person.setfName(fName);
        person.setlName(lName);
        saveBtn.setEnabled(false);
        updateUserBtn.setEnabled(true);
        fNameTxt.setEnabled(false);
        lNameTxt.setEnabled(false);
        JOptionPane.showMessageDialog(null, "Person Updated Successfully");
    }//GEN-LAST:event_saveBtnActionPerformed


